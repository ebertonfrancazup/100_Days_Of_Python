# Tipo de dado bool (boolean)
# Ao questionar algo em um programa
# Só existem duas respostas possíveis
# Sim (True) ou não (False)
# Existem vários operadores para "questionar"
# Questiona se um valor é igual ao outro
# str, int, float, bool


#Operador lógico == 

print(10 == 10)
print(10 == 9)
print(type(True))
print(type(False))

#Converter um tipo em outro tipo
#Polimorfismo no Python (Ocorre porquê é dinâmico) - Ele entende o tipo informado

print(1 + 1)
print('a' + 'b')
print(int('1') + 1)
print(bool(' ')) #True
print(bool('')) #False
